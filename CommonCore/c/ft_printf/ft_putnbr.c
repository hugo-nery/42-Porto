#include "ft_printf.h"

int	ft_putnbr(long n)
{
	int	len;

	len = 0;
	if (n < 0)
	{
		len += ft_putchr('-');
		n *= -1;
	}
	if (n > 9)
		len += ft_putnbr (n / 10);
	len += ft_putchr("0123456789"[n % 10]);
	return (len);
}
