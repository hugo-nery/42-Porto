#include <unistd.h>

void ft_putnbr(int nbr)
{
	long n;

	n = nbr;
	
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		write(1, &"0123456789"[n % 10], 1);
	}
	else
		write(1, &"0123456789"[n], 1);
}

int main ()
{
	ft_putnbr(-19234);

	return 0;
}