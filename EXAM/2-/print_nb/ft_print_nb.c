#include <unistd.h>

void print_nb(int num);

int main() {


}


void print_nb(int num)
{
    long n = num;
    if (n < 0)
    {
        write (1, '-', 1);
        n = -n;
    }
    if (n >= 10)
        print_nb (n / 10);
    write (1, &"0123456789"[n % 10], 1);
}

