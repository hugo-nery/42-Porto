#include "my_header.h"

void ft_putnbr (int nb)
{
	long n = nb;

	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr (n / 10);
		
	write(1, &("0123456789"[n % 10]), 1);
}
